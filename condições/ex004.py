import math
num = float(input('Digite aqui um número para saber o seu dobro, triplo e sua raiz quadrada: '))
dobro = num * 2 
triplo = num * 3
raiz = math.sqrt(num)
print(f'Aqui está o dobro de {num:} = {dobro}')
print(f'Aqui está o triplo de {num} = {triplo}')
print(f'Aqui está a raiz quadrara de {num} = {raiz}')